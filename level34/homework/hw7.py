# 7) შექმენით ფუნქცია. შექმენით რიცხვებით სავსე სია. დათვალეთ თუ რამდენჯერ არის შემდეგი რიცხვი წინაზე მეტი. მაგალითად: თუ მოცემული გვაქვს სია [1, 3, 2, 5, 4], მაშინ უნდა დავპრინტოთ 2, რადგან 3 > 1, და 5 > 2. გამოიყენე ინდექსები. გამოიძახეთ ფუნქცია.

def number():
    numbers = [1, 3, 2, 5, 4]
    count = 0

    for i in range(1, len(numbers)):
        if numbers[i] > numbers[i - 1]:
            count += 1

    print(count)

number()
