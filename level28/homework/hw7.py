# 7) მომხმარებელს შეაყვანინე რიცხვი 1–5. დაბეჭდე სიტყვებით, მაგალითად: 1 → One 2 → Two გამოიყენე if / elif / else

number = int(input("Enter number:"))

if number == 1:
    print("One")
elif number == 2:
    print("Two")
elif number == 3:
    print("Three")
elif number == 4:
    print("Four")
elif number ==5:
    print("Five")
else:
    print("მხოლოდ 1-5 რიცხვები:")
    