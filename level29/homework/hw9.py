# 9)კომენტარის სახით ჩამოწერეთ სიის ყველა ფუნქცია და მიუწერეთ თ რომელი რას აკეთებს

# 1) append - სიაში ვამატებთ ახალ ელემენტს
# 2) insert - სიაში ვსვავთ ახალ ელემენტს სადაც გვინდა
# 3) pop - სიიდან ამოგვაქვს ის ელემენტი რომელიც გვინდა
# 4) clear - ვასუფთავებთ მთლიანად სიას
# 5) remove - სიიდან ვშლით პირველივე ელემენტს
# 6) index - სიაში აბრუნებს პირველივე შემხვედრ ინდექსს