#     თუ ლუწია, დაბეჭდე "დადებითი ლუწი".

#     თუ არა — "დადებითი კენტი".

# სხვა შემთხვევაში დაბეჭდე --> "რიცხვი უარყოფითია"

num = int(input("შეიყვანე რიცხვი: "))

if num > 0:
    if num % 2 == 0:
        print("დადებითი ლუწი")
    else:
        print("დადებითი კენტი")
else:
    print("რიცხვი უარყოფითია")
